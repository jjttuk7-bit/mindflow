const mkHeader = (t) => new Header({ children: [new Paragraph({ alignment: AlignmentType.RIGHT, children: [new TextRun({ text: `Mindflow \u2014 ${t}`, font: "Arial", size: 16, color: "999999" })] })] });
const mkFooter = () => new Footer({ children: [new Paragraph({ alignment: AlignmentType.CENTER, children: [new TextRun({ text: "Page ", font: "Arial", size: 16, color: "999999" }), new TextRun({ children: [PageNumber.CURRENT], font: "Arial", size: 16, color: "999999" })] })] });

const h1 = (t) => new Paragraph({ heading: HeadingLevel.HEADING_1, children: [new TextRun(t)] });
const h2 = (t) => new Paragraph({ heading: HeadingLevel.HEADING_2, children: [new TextRun(t)] });
const h3 = (t) => new Paragraph({ heading: HeadingLevel.HEADING_3, children: [new TextRun(t)] });
const p = (t, o = {}) => new Paragraph({ spacing: { after: 120 }, ...o.pOpts, children: [new TextRun({ text: t, ...o })] });
const sp = () => new Paragraph({ spacing: { after: 120 }, children: [] });
const pb = () => new Paragraph({ children: [new PageBreak()] });
const b = (t, lv = 0) => new Paragraph({ numbering: { reference: "bullets", level: lv }, spacing: { after: 60 }, children: [new TextRun({ text: t, size: 22 })] });
const bb = (label, desc) => new Paragraph({ numbering: { reference: "bullets", level: 0 }, spacing: { after: 60 }, children: [new TextRun({ text: label, bold: true, size: 22 }), new TextRun({ text: ` \u2014 ${desc}`, size: 22 })] });
const st = (t) => new Paragraph({ numbering: { reference: "steps", level: 0 }, spacing: { after: 80 }, children: [new TextRun({ text: t, size: 22 })] });

function tblRow(cells, hdr = false) {
  return new TableRow({ children: cells.map((t, i) => new TableCell({
    borders, width: { size: cells.length === 2 ? (i === 0 ? 3500 : 5860) : Math.floor(9360 / cells.length), type: WidthType.DXA },
    shading: hdr ? { fill: HEADER_BG, type: ShadingType.CLEAR } : undefined, margins: cellMargins,
    children: [new Paragraph({ children: [new TextRun({ text: t, bold: hdr, size: 20, font: "Arial" })] })]
  }))});
}
function tbl(headers, rows) {
  const cw = Math.floor(9360 / headers.length);
  return new Table({ width: { size: 9360, type: WidthType.DXA }, columnWidths: Array(headers.length).fill(cw), rows: [tblRow(headers, true), ...rows.map(r => tblRow(r))] });
}

function titlePage(title, subtitle) {
  return [
    new Paragraph({ spacing: { before: 3000 }, children: [] }),
    new Paragraph({ alignment: AlignmentType.CENTER, spacing: { after: 200 }, children: [new TextRun({ text: "Mindflow", font: "Georgia", size: 72, bold: true, color: BRAND })] }),
    new Paragraph({ alignment: AlignmentType.CENTER, spacing: { after: 400 }, children: [new TextRun({ text: title, font: "Arial", size: 32, color: "666666" })] }),
    new Paragraph({ alignment: AlignmentType.CENTER, spacing: { after: 200 }, children: [new TextRun({ text: subtitle, font: "Arial", size: 22, color: "999999", italics: true })] }),
    new Paragraph({ alignment: AlignmentType.CENTER, spacing: { before: 800 }, children: [new TextRun({ text: "2026\uB144 3\uC6D4 \uAE30\uC900", font: "Arial", size: 20, color: "AAAAAA" })] }),
    pb(),
  ];
}

function mkDoc(headerTitle, children) {
  return new Document({
    styles: styles(), numbering: numbering(),
    sections: [{ properties: { page: { size: { width: 12240, height: 15840 }, margin: { top: 1440, right: 1440, bottom: 1440, left: 1440 } } },
      headers: { default: mkHeader(headerTitle) }, footers: { default: mkFooter() }, children }]
  });
}

